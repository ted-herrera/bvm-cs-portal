import { NextResponse } from "next/server";
import JSZip from "jszip";
import { getClient } from "@/lib/mock-data";
import { getBuildByClientId } from "@/lib/store";
import { generateSiteHTML } from "@/lib/studio-engine";

type LookKey = "warm_bold" | "professional" | "bold_modern";

function buildBriefMarkdown(params: {
  businessName: string;
  city: string;
  zip: string;
  services: string[];
  tagline: string;
  look: string;
  cta: string;
  sbr: Record<string, unknown> | null;
  phone: string;
}): string {
  const { businessName, city, zip, services, tagline, look, cta, sbr, phone } = params;
  const sbrLines: string[] = [];
  if (sbr) {
    if (sbr.campaignHeadline) sbrLines.push(`- **Campaign headline:** ${sbr.campaignHeadline}`);
    if (sbr.localAdvantage) sbrLines.push(`- **Local advantage:** ${sbr.localAdvantage}`);
    if (Array.isArray(sbr.competitors) && sbr.competitors.length > 0) {
      sbrLines.push(`- **Competitors:** ${(sbr.competitors as string[]).join(", ")}`);
    }
    if (sbr.targetDemo) sbrLines.push(`- **Target demo:** ${sbr.targetDemo}`);
    if (sbr.uniqueAngle) sbrLines.push(`- **Unique angle:** ${sbr.uniqueAngle}`);
    if (sbr.toneNotes) sbrLines.push(`- **Tone:** ${sbr.toneNotes}`);
  }

  return `# ${businessName} — Build Brief

## Business
- **Name:** ${businessName}
- **Location:** ${city}${zip ? `, ${zip}` : ""}
- **Phone:** ${phone || "—"}
- **Look:** ${look}
- **Tagline:** ${tagline || "—"}
- **CTA:** ${cta}

## Services
${services.length > 0 ? services.map((s) => `- ${s}`).join("\n") : "- (none)"}

## SBR Market Intelligence
${sbrLines.length > 0 ? sbrLines.join("\n") : "_No SBR data available._"}

---
Generated by BVM Design Center.
`;
}

function buildQaChecklistMarkdown(businessName: string, services: string[], phone: string): string {
  return `# QA Checklist — ${businessName}

Run through every item before re-uploading to the QA tool.

## Structure
- [ ] HTML has \`lang="en"\` on the \`<html>\` tag
- [ ] \`<title>\` tag is populated with business name
- [ ] Meta description is present
- [ ] Viewport meta tag is present
- [ ] Canonical link tag added
- [ ] Open Graph og:title and og:description present

## Compliance
- [ ] Every \`<img>\` has a descriptive \`alt=\` attribute (e.g. \`alt="${businessName} storefront"\`)
- [ ] All buttons have visible or \`aria-label\` text
- [ ] Heading hierarchy is valid (h1 → h2 → h3, no gaps)
- [ ] Color contrast meets WCAG AA

## Content
- [ ] Business name appears prominently in hero
- [ ] Phone number \`${phone || "(not provided — confirm with rep)"}\` is present and clickable (\`tel:\`)
- [ ] Address appears in footer
- [ ] CTA button matches the brief (${services[0] || "primary service"})
- [ ] No placeholder text (Lorem, [INSERT], etc.)
- [ ] All listed services are represented: ${services.length > 0 ? services.join(", ") : "(none)"}

## Performance
- [ ] Images have width/height attributes
- [ ] No blocking scripts in \`<head>\`
- [ ] Inline styles kept under 5

## Known issues flagged automatically
- ⚠️ Run the QA engine to pick up any automatable fixes (alt text, canonical, og tags).

---
Once every box is checked, re-upload \`index.html\` to the QA tool, confirm the score is ≥70, then mark the build ready.
`;
}

function buildReadmeMarkdown(businessName: string): string {
  return `# ${businessName} — BVM Dev Pack

This ZIP contains everything you need to build and ship this site.

## Files
- \`index.html\` — the generated site HTML, ready to open in a browser
- \`brief.md\` — client brief with business details, tagline, look, CTA, and SBR market intelligence
- \`qa-checklist.md\` — pre-filled QA checklist with known issues flagged
- \`sbr-data.json\` — full SBR market intelligence (raw JSON)
- \`README.md\` — this file

## Workflow
1. Open \`index.html\` in a browser and review against \`brief.md\`.
2. Fix the issues listed in \`qa-checklist.md\`.
3. Re-upload the updated \`index.html\` to the QA tool in the Design Center.
4. When the QA score is ≥ 70, click **MARK READY FOR QA** in the build queue.
5. Deploy to Vercel or Netlify, paste the live URL back into the Design Center, and click **MARK BUILD COMPLETE**.

Happy shipping.
`;
}

export async function GET(request: Request) {
  const { searchParams } = new URL(request.url);
  const clientId = searchParams.get("clientId");

  if (!clientId) {
    return NextResponse.json({ error: "clientId required" }, { status: 400 });
  }

  const client = getClient(clientId);
  if (!client) {
    return NextResponse.json({ error: "Client not found" }, { status: 404 });
  }

  const build = getBuildByClientId(clientId);

  const lookKey = ((client.selectedLook || build?.look || "professional") as LookKey);
  const siteHTML = build?.generatedSiteHTML || generateSiteHTML(client, lookKey);

  const services =
    build?.services ||
    (client.intakeAnswers?.q3 || "")
      .split(",")
      .map((s) => s.trim())
      .filter(Boolean);

  const sbr = (client.sbrData || build?.sbrData || null) as Record<string, unknown> | null;
  const tagline =
    (sbr?.suggestedTagline as string) ||
    (sbr?.tagline as string) ||
    build?.tagline ||
    "";
  const cta = client.intakeAnswers?.q4 || build?.cta || "Contact Us";

  const zip = new JSZip();
  zip.file("index.html", siteHTML);
  zip.file(
    "brief.md",
    buildBriefMarkdown({
      businessName: client.business_name,
      city: client.city,
      zip: client.zip,
      services,
      tagline,
      look: lookKey,
      cta,
      sbr,
      phone: client.phone,
    }),
  );
  zip.file("qa-checklist.md", buildQaChecklistMarkdown(client.business_name, services, client.phone));
  zip.file("sbr-data.json", JSON.stringify(sbr || {}, null, 2));
  zip.file("README.md", buildReadmeMarkdown(client.business_name));

  const buf = await zip.generateAsync({ type: "uint8array" });

  const slug = client.business_name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/-+$/, "");
  const date = new Date().toISOString().split("T")[0];
  const filename = `BVM_DevPack_${slug}_${date}.zip`;

  return new NextResponse(buf as unknown as BodyInit, {
    headers: {
      "Content-Type": "application/zip",
      "Content-Disposition": `attachment; filename="${filename}"`,
      "Content-Length": String(buf.byteLength),
    },
  });
}
